<h3>How to train OC Volume to work with your voice?</h3>
<ol>
<li>Download <b>training.zip</b> from download page and extract to your JDK directory.
<li>Run <b>training.java</b> using JDK<br>
<img src="images/dos.jpg">
<li>After running <b>training.java</b> you should see the following window:<br>
<img src="images/training.jpg">
<li>Click "File" from the menu, then click "New" to start training<br>
<img src="images/select.new.jpg">
<li>In the next screen type in the word you want to train then click "OK". For example: backward<br>
<img src="images/new.word.jpg">
<li>After you have entered the word to train, you should select a location to save the word configuration to. <b>IMPORTANT:</b> The file name should be the same as the word and the file extension should be ".vq"<br>
<img src="images/save.word.jpg">
<li>Once you have selected the location to save the configuration file to, you can start recording samples to train the recognition system. Click "record" once and say the word you want to train, then click "stop". Repeat the process until you have recorded 20 times (Note: The default number of samples is 20, but you can change this).<br>
<img src="images/record.sample.jpg">
</ol>
