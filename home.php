<h3>WHAT is OC Volume?</h3>
OC Volume was created by Chien-Wen Danny Su, Keith Fung and Andrei Leonov. Its aim is to enable voice communication with computers. OC Volume is a speech recognition engine written in Java, and should be integrated with other applications to create voice-driven software.
<br/><br/>
If you have questions, please contact project lead Danny Su at contact@dannysu.com.
<br><br>
<h3>WHEN did we start the project?</h3>
The OC Volume project started on March 28, 2002.
<br><br>
<h3>WHEN did the project finish?</h3>
This project will never finish since there is still a "life" after we finish coding.
We receive feedbacks from the users of OC Volume, and we still provide help to people who want to use our software. Please send us an email about how you're using OC Volume, we would love to hear about it!
<br><br>
<h3>WHY free and open-source?</h3>
We have benefited from many open-source projects, either directly or indirectly.
Therefore our aim is not to make profit but to learn and to contribute back to the open-source community.
